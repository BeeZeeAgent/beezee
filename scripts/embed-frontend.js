#!/usr/bin/env bun
// Reads all files from frontend/dist and writes frontend-embed.js
// so they can be bundled into the standalone binary by `bun build --compile`.

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const distDir = path.join(__dirname, '..', 'frontend', 'dist');
const outFile = path.join(__dirname, '..', 'frontend-embed.js');

function walk(dir, base = dir) {
  const results = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) results.push(...walk(full, base));
    else results.push('/' + path.relative(base, full).replace(/\\/g, '/'));
  }
  return results;
}

const MIME = {
  '.html': 'text/html; charset=utf-8',
  '.js':   'application/javascript; charset=utf-8',
  '.css':  'text/css; charset=utf-8',
  '.svg':  'image/svg+xml',
  '.png':  'image/png',
  '.ico':  'image/x-icon',
  '.json': 'application/json',
  '.woff2': 'font/woff2',
  '.woff': 'font/woff',
  '.ttf':  'font/ttf',
};

const files = walk(distDir);
const entries = files.map(route => {
  const buf = fs.readFileSync(path.join(distDir, route));
  const ext = path.extname(route);
  return [route, { data: buf.toString('base64'), mime: MIME[ext] || 'application/octet-stream' }];
});

// Also embed terminal.html from the project root
const terminalHtml = path.join(__dirname, '..', 'terminal.html');
if (fs.existsSync(terminalHtml)) {
  entries.push(['/terminal.html', {
    data: fs.readFileSync(terminalHtml).toString('base64'),
    mime: 'text/html; charset=utf-8',
  }]);
}

const totalKB = entries.reduce((n, [, v]) => n + v.data.length * 0.75, 0) / 1024;

const code = `// AUTO-GENERATED by scripts/embed-frontend.js — do not edit manually
export const embeddedFrontend = new Map(${JSON.stringify(entries)});
`;

fs.writeFileSync(outFile, code);
console.log(`Embedded ${files.length} files (${totalKB.toFixed(0)} KB uncompressed)`);
